public class Main {
    public static void main(String[] args) {
        // Создание экземпляра потока
        Поток поток = new Поток();

        // Создание групп студентов
        УчебнаяГруппа группа1 = new УчебнаяГруппа("Группа 1");
        УчебнаяГруппа группа2 = new УчебнаяГруппа("Группа 2");

        // Добавление групп в поток
        поток.добавитьГруппу(группа1);
        поток.добавитьГруппу(группа2);

        // Создание студентов
        Студент студент1 = new Студент("Иван", 80);
        Студент студент2 = new Студент("Петр", 75);

        // Сравнение оценок студентов
        ОценкаСтудентаComparator comparator = new ОценкаСтудентаComparator();
        int результат = comparator.compare(студент1, студент2);

        // Вывод результата сравнения
        if (результат < 0) {
            System.out.println("Оценка студента " + студент1.getИмя() + " выше, чем у студента " + студент2.getИмя());
        } else if (результат > 0) {
            System.out.println("Оценка студента " + студент2.getИмя() + " выше, чем у студента " + студент1.getИмя());
        } else {
            System.out.println("Оценки студентов равны");
        }
    }
}
